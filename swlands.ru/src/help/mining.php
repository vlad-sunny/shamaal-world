<div align="justify" class=small>Познания в горном деле позволяют вашему персонажу разрабатывать залежи руды в шахтах по всему миру Шамаал. Добытая руда перерабатывается в слитки, используемые кузнецами для изготовления оружия, доспехов и других полезных предметов.<br><br>Чтобы приступить к добыче руды, необходимо взять кирку и найти комнату с залежами руды. Чем выше мастерство вашего героя в горном деле, тем более редкие руды он сможет добывать.<br><br>Чтобы переработать добытую руду в слитки, необходимо найти кузницу с функцией плавки металла. Количество слитков, получаемых после переплавки, также зависит от мастерства в горном деле.</div>

